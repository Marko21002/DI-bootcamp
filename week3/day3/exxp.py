def print_builtin_functions():
    """
    This function demonstrates the use of three built-in functions in Python: abs(), int(), and input(). 
    It prints the results of calling each function with sample input values.

    Parameters: 
    None

    Returns: 
    None
    """
    # Using abs() function to get absolute value of -10
    print("Absolute value of -10: ", abs(-10))

    # Using int() function to convert string "123" to integer
    print("Converting '123' to integer: ", int('123'))

    # Using input() function to get user input and printing it
    user_input = input("Enter your name: ")
    print("Hello,", user_input)


print_builtin_functions()
# The above program defines a function print_builtin_functions() which demonstrates the use of three built-in functions in Python: abs(), int(), and input(). The function takes no parameters and returns nothing.

# The function starts by calling the abs() function with an argument of -10 and printing the result, which is the absolute value of -10. The next line calls the int() function with the argument '123', which converts the string '123' to an integer, and prints the result. Finally, the function calls the input() function to get user input, and prints a greeting message using the user's input.

# This program serves as an example of how to use built-in functions in Python, and demonstrates how these functions can be useful in various scenarios.
